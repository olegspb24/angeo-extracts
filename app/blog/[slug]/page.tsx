export async function generateMetadata({ params }: { params: { slug: string } | Promise<{ slug: string }> }) {
  const resolvedParams = params instanceof Promise ? await params : params;
  const article = articles[resolvedParams.slug];
  
  if (!article) {
    return {
      title: 'Статья не найдена'
    };
  }
  
  return {
    title: `${article.title} | Angel Extracts`,
    description: article.content.substring(0, 160)
  };
}

export default async function BlogPost({ params }: { params: { slug: string } | Promise<{ slug: string }> }) {
  const resolvedParams = params instanceof Promise ? await params : params;
  const article = articles[resolvedParams.slug];

  if (!article) {
    notFound();
  }

  return (
    <div style={{
      minHeight: '100vh',
      backgroundColor: '#FFFFFF',
      padding: '40px 16px'
    }}>
      <div style={{
        maxWidth: '900px',
        margin: '0 auto'
      }}>
        <article>
          <h1 style={{
            fontSize: 'clamp(32px, 5vw, 48px)',
            fontWeight: 700,
            color: '#1a1a1a',
            marginBottom: '32px',
            lineHeight: 1.2
          }}>
            {article.title}
          </h1>

          <div 
            style={{
              fontSize: '18px',
              lineHeight: 1.8,
              color: '#444'
            }}
            dangerouslySetInnerHTML={{
              __html: article.content
                .split('\n\n')
                .map((paragraph) => {
                  if (paragraph.startsWith('**') && paragraph.endsWith('**')) {
                    return `<h2 style="font-size: 24px; font-weight: 700; color: #1a1a1a; margin-top: 32px; margin-bottom: 16px;">${paragraph.replace(/\*\*/g, '')}</h2>`;
                  }
                  return `<p style="margin-bottom: 20px;">${paragraph}</p>`;
                })
                .join('')
            }}
          />

          <a 
            href="/#blog" 
            style={{
              color: '#3F8A00',
              textDecoration: 'none',
              fontSize: '16px',
              fontWeight: 600,
              display: 'inline-block',
              marginTop: '40px'
            }}
          >
            ← Назад к блогу
          </a>
        </article>
      </div>
    </div>
  );
}
